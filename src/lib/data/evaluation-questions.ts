export type ResponseMap = Record<string, LikertScore>;

export function interpretScore(score: number): { label: string; tone: string } {
	if (score >= 4.21) return { label: 'Excellent', tone: 'emerald' };
	if (score >= 3.41) return { label: 'Very Good', tone: 'sky' };
	if (score >= 2.61) return { label: 'Good', tone: 'amber' };
	if (score >= 1.81) return { label: 'Fair', tone: 'orange' };
	if (score > 0) return { label: 'Poor', tone: 'red' };
	return { label: 'No data', tone: 'slate' };
}
